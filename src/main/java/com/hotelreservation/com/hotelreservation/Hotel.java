package com.hotelreservation.com.hotelreservation ;

public class Hotel {
    public  String name;
    public  Double rates;

    public Hotel() {
        this.name = name;
        this.rates = rates;
    }
    public String getName() {
        return name;
    }
    public void setName(String name){
        this.name = name;
    }
    public Double getRate(){
        return rates;
    }
    public void setRate(double rates){
        this.rates = rates;
    }
}